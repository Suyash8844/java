// Write a program and print whether it is positive and negative
class Program3{
	public static void main(String[] args){
		int num=0;
		if(num>0){
			System.out.println(num+ " is Positive");
		}else if(num<0){
			System.out.println(num+ " is Negative");
		}else{
			System.out.println("Zero");
		}
	}
}
