// WAP in java to accept three numbers and check whether they are pythagorean triplet or not
class Program9{
	public static void main(String[] args){
		int a=2;
		int b=2;
		int c=2;
		if(a*a+b*b==c*c){
			System.out.println("It is Pythagoras Triplet");
		}else{
			System.out.println("Not a pythagoras triplet");
		}
	}
}
