// write a java program to take a number and print whether it is less than 10 or greater than 10
class Program2{
	public static void main(String[] args){
		int no=5;
		if(no<10){
			System.out.println(no+" is less than 10");
		}else if(no>10){
			System.out.println(no+" is greater than 10");
		}else{
			System.out.println(no+" is equal to 10");
		}
	}
}
